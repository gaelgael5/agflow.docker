_TEMPLATE_RE = re.compile(r"(?<!\$)\{(\w+)\}")
# Matches ${VAR} and ${VAR:-default} — standard shell templating that the
# user would expect to be expanded before docker sees it. Because we call
# docker via HTTP API (no shell in between), we do the expansion ourselves
# against os.environ.
_SHELL_VAR_RE = re.compile(r"\$\{(\w+)(?::-([^}]*))?\}")


def _generate_tmp_files(
    dockerfile_id: str,
    container_name: str,
    config: dict[str, Any],
    task_payload: dict[str, Any] | None = None,
) -> str:
    """Generate .env + run.sh (+ task.json if task_payload) in {AGFLOW_DATA_DIR}/{dockerfile_id}/.tmp/.

    Returns the absolute path of the generated run.sh. When task_payload is
    provided, run.sh is generated in "task mode": ``docker run -i … < task.json``
    so the container's entrypoint reads the task on stdin.
    """
    import json as _json

    data_dir = os.environ.get("AGFLOW_DATA_DIR", "/app/data")
    tmp_dir = os.path.join(data_dir, dockerfile_id, ".tmp")
    os.makedirs(tmp_dir, exist_ok=True)

    # .env — all resolved env vars
    env_list = config.get("Env", [])
    env_content = "\n".join(env_list) + "\n"
    with open(os.path.join(tmp_dir, ".env"), "w", encoding="utf-8") as f:
        f.write(env_content)

    # task.json — payload piped to the container's entrypoint via stdin
    if task_payload is not None:
        task_path = os.path.join(tmp_dir, "task.json")
        with open(task_path, "w", encoding="utf-8") as f:
            _json.dump(task_payload, f, ensure_ascii=False)
            f.write("\n")

    # run.sh
    lines = [
        "#!/usr/bin/env bash",
        "set -euo pipefail",
        "",
        "# Generated by agflow — do not edit",
        'SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"',
        'set -a; source "$SCRIPT_DIR/.env"; set +a',
        "",
    ]
    cmd_parts = ["docker run --rm"]
    if task_payload is not None:
        cmd_parts.append("-i")
    cmd_parts.append(f"--name {container_name}")
    host_config = config.get("HostConfig", {})
    if host_config.get("NetworkMode"):
        cmd_parts.append(f"--network {host_config['NetworkMode']}")
    if host_config.get("Init"):
        cmd_parts.append("--init")
    for label_key, label_value in (config.get("Labels") or {}).items():
        cmd_parts.append(f'--label "{label_key}={label_value}"')
    for bind in host_config.get("Binds", []):
        cmd_parts.append(f'-v "{bind}"')
    if host_config.get("Memory"):
        mem_mb = host_config["Memory"] // (1024 * 1024)
        cmd_parts.append(f"--memory {mem_mb}m")
    if host_config.get("NanoCpus"):
        cpus = host_config["NanoCpus"] / 1_000_000_000
        cmd_parts.append(f"--cpus {cpus}")
    if config.get("WorkingDir"):
        cmd_parts.append(f"-w {config['WorkingDir']}")
    for env_line in env_list:
        key = env_line.split("=", 1)[0]
        cmd_parts.append(f'-e "{key}=${{{key}}}"')
    cmd_parts.append(config.get("Image", ""))
    docker_cmd = " \\\n  ".join(cmd_parts)
    if task_payload is not None:
        docker_cmd += ' < "$SCRIPT_DIR/task.json"'
    lines.append(docker_cmd)
    lines.append("")

    run_path = os.path.join(tmp_dir, "run.sh")
    with open(run_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    os.chmod(run_path, 0o755)

    _log.info("container.generate_tmp", dockerfile_id=dockerfile_id, tmp_dir=tmp_dir)
    return run_path


async def _load_platform_secrets() -> dict[str, str]:
    """Load all global platform secrets as a dict for env var injection."""
    from agflow.config import get_settings
    from agflow.db.pool import fetch_all

    master = get_settings().secrets_master_key
    rows = await fetch_all(
        """
        SELECT var_name, pgp_sym_decrypt(value_encrypted, $1) AS value
        FROM secrets
        WHERE scope = 'global'
        """,
        master,
    )
    return {r["var_name"]: r["value"] for r in rows}


