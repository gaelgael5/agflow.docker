def _parse_docker_ts(raw: str) -> datetime:
    # Docker returns ISO-8601 with nanosecond precision; trim to microseconds
    # so datetime.fromisoformat is happy. "2024-01-02T03:04:05.123456789Z"
    # → "2024-01-02T03:04:05.123456+00:00".
    s = raw.rstrip("Z")
    if "." in s:
        head, frac = s.split(".", 1)
        frac = frac[:6]
        s = f"{head}.{frac}"
    return datetime.fromisoformat(s + "+00:00")


def _info_from_container(raw: dict[str, Any]) -> ContainerInfo:
    labels = raw.get("Labels") or {}
    names = raw.get("Names") or []
    name = (names[0] if names else raw.get("Name", "")).lstrip("/")
    created = raw.get("Created")
    if isinstance(created, (int, float)):
        created_at = datetime.fromtimestamp(created)
    elif isinstance(created, str):
        created_at = _parse_docker_ts(created)
    else:
        created_at = datetime.now()
    return ContainerInfo(
        id=raw.get("Id", raw.get("ID", "")),
        name=name,
        dockerfile_id=labels.get(_AGFLOW_DOCKERFILE_LABEL, ""),
        image=raw.get("Image", ""),
        status=raw.get("State", "created"),
        created_at=created_at,
        instance_id=labels.get(_AGFLOW_INSTANCE_LABEL, ""),
    )


async def list_running() -> list[ContainerInfo]:
    """List all agflow-managed containers (running or stopped)."""
    docker = aiodocker.Docker()
    try:
        containers = await docker.containers.list(
            all=True,
            filters={"label": [f"{_AGFLOW_MANAGED_LABEL}=true"]},
        )
        result: list[ContainerInfo] = []
        for c in containers:
            # Show() returns the full inspect payload which has Config.Labels.
            inspect = await c.show()
            cfg = inspect.get("Config") or {}
            state = inspect.get("State") or {}
            labels = cfg.get("Labels") or {}
            created_at = _parse_docker_ts(inspect.get("Created", ""))
            result.append(
                ContainerInfo(
                    id=inspect.get("Id", ""),
                    name=(inspect.get("Name") or "").lstrip("/"),
                    dockerfile_id=labels.get(_AGFLOW_DOCKERFILE_LABEL, ""),
                    image=cfg.get("Image", ""),
                    status=state.get("Status", "created"),
                    created_at=created_at,
                    instance_id=labels.get(_AGFLOW_INSTANCE_LABEL, ""),
                )
            )
        return result
    finally:
        await docker.close()


async def start(
    dockerfile_id: str,
    *,
    params_json_content: str,
    content_hash: str,
    user_secrets: dict[str, str] | None = None,
) -> ContainerInfo:
    """Create and start a container for a given dockerfile.

    Raises:
        ImageNotBuiltError: the target image does not exist yet.
        TooManyContainersError: the hard limit of running containers is reached.
        InvalidParamsError: Dockerfile.json cannot be translated to a config.
    """
    # Enforce concurrency limit before touching docker.
    existing = await list_running()
    alive = [c for c in existing if c.status in ("running", "created", "restarting")]
    if len(alive) >= MAX_RUNNING_CONTAINERS:
        raise TooManyContainersError(
            f"Maximum of {MAX_RUNNING_CONTAINERS} running containers reached. "
            f"Stop one before launching another."
        )

    instance_id = secrets.token_hex(3)
    platform_secrets = await _load_platform_secrets()
    # Merge: platform secrets + user secrets (user wins)
    all_secrets = {**platform_secrets, **(user_secrets or {})}
    name, config = build_run_config(
        dockerfile_id=dockerfile_id,
        params_json_content=params_json_content,
        content_hash=content_hash,
        instance_id=instance_id,
        extra_env=all_secrets,
    )

    # Pre-create auto-prefixed mount directories so the default workspace/
    # output/... mounts "just work" without the user having to mkdir first.
    _ensure_mount_paths_from_config(
        dockerfile_id, params_json_content, instance_id, content_hash
    )

    # Generate .tmp/ files (run.sh + .env) before launching
    _generate_tmp_files(dockerfile_id, name, config)

    docker = aiodocker.Docker()
    try:
        try:
            await docker.images.inspect(config["Image"])
        except aiodocker.exceptions.DockerError as exc:
            if exc.status == 404:
                raise ImageNotBuiltError(
                    f"Image '{config['Image']}' not found — build the dockerfile first."
                ) from exc
            raise

        container = await docker.containers.create(config=config, name=name)
        await container.start()
        inspect = await container.show()

        cfg = inspect.get("Config") or {}
        state = inspect.get("State") or {}
        labels = cfg.get("Labels") or {}
        info = ContainerInfo(
            id=inspect.get("Id", ""),
            name=(inspect.get("Name") or "").lstrip("/"),
            dockerfile_id=labels.get(_AGFLOW_DOCKERFILE_LABEL, dockerfile_id),
            image=cfg.get("Image", config["Image"]),
            status=state.get("Status", "running"),
            created_at=_parse_docker_ts(inspect.get("Created", "")),
            instance_id=labels.get(_AGFLOW_INSTANCE_LABEL, instance_id),
        )
        _log.info(
            "container.start",
            dockerfile_id=dockerfile_id,
            container_id=info.id,
            name=info.name,
        )
        return info
    finally:
        await docker.close()


async def run_task(
    dockerfile_id: str,
    *,
    params_json_content: str,
    content_hash: str,
    task_payload: dict[str, Any],
    timeout_seconds: int = 600,
    user_secrets: dict[str, str] | None = None,
    on_container_started: Any | None = None,
    cleanup: bool = False,
    session_id: str | None = None,
) -> AsyncIterator[dict[str, Any]]:
    """One-shot: regenerate .tmp/{.env, run.sh, task.json} then exec run.sh.

    The launch uses the exact ``run.sh`` the user can run by hand for
    diagnosis — same .env, same docker run command, same stdin pipe. We
    subprocess ``bash run.sh`` and stream its stdout as newline-delimited
    JSON events.

    Yields parsed event dicts as they arrive, plus a final
    ``{"type": "done", "status": "success" | "failure", "exit_code": N}``.

    When ``cleanup=True`` (Module 1 chat de test), ``.tmp/`` is removed and
    any surviving container is force-deleted after the task. Module 4
    launches keep the generated files as the production artefact.
    """
    import asyncio as _asyncio
    import json as _json
    import secrets as _secrets
    import shutil as _shutil

    import aiodocker as _aiodocker

    # Concurrency guard — shares the same limit as interactive containers.
    existing = await list_running()
    alive = [
        c for c in existing if c.status in ("running", "created", "restarting")
    ]
    if len(alive) >= MAX_RUNNING_CONTAINERS:
        raise TooManyContainersError(
            f"Maximum of {MAX_RUNNING_CONTAINERS} running containers reached."
        )

    session_id = session_id or _secrets.token_hex(6)
    instance_id = secrets.token_hex(3)
    platform_secrets = await _load_platform_secrets()
    all_secrets = {**platform_secrets, **(user_secrets or {})}
    name, config = build_run_config(
        dockerfile_id=dockerfile_id,
        params_json_content=params_json_content,
        content_hash=content_hash,
        instance_id=instance_id,
        extra_env=all_secrets,
    )
    _ensure_mount_paths_from_config(
        dockerfile_id, params_json_content, instance_id, content_hash
    )

    # Pre-flight: image must exist, otherwise fail early with a typed error
    # instead of letting docker CLI spew to stderr.
    _aio = _aiodocker.Docker()
    try:
        try:
            await _aio.images.inspect(config["Image"])
        except _aiodocker.exceptions.DockerError as exc:
            if exc.status == 404:
                raise ImageNotBuiltError(
                    f"Image '{config['Image']}' not found — build the dockerfile first."
                ) from exc
            raise
    finally:
        await _aio.close()

    from agflow.db.pool import get_pool
    from agflow.mom.envelope import Direction, Kind
    from agflow.mom.publisher import MomPublisher

    _mom_pool = await get_pool()
    _mom_publisher = MomPublisher(
        pool=_mom_pool,
        groups_config={
            Direction.IN: ["dispatcher"],
            Direction.OUT: ["ws_push", "router"],
        },
    )
    _in_msg_id = await _mom_publisher.publish(
        session_id=session_id,
        instance_id=instance_id,
        direction=Direction.IN,
        source="system",
        kind=Kind.INSTRUCTION,
        payload=task_payload,
    )
    _adapter = get_adapter(dockerfile_id)

    # Regenerate .tmp/{.env, run.sh, task.json} — run.sh is the source of
    # truth for the launch.
    run_sh = _generate_tmp_files(
        dockerfile_id=dockerfile_id,
        container_name=name,
        config=config,
        task_payload=task_payload,
    )
    tmp_dir = os.path.dirname(run_sh)

    proc: _asyncio.subprocess.Process | None = None
    try:
        proc = await _asyncio.create_subprocess_exec(
            "bash",
            run_sh,
            stdout=_asyncio.subprocess.PIPE,
            stderr=_asyncio.subprocess.STDOUT,
        )

        if on_container_started is not None:
            await on_container_started("", name)

        assert proc.stdout is not None

        deadline = _asyncio.get_event_loop().time() + max(1, timeout_seconds)
        timed_out = False
        try:
            while True:
                remaining = deadline - _asyncio.get_event_loop().time()
                if remaining <= 0:
                    timed_out = True
                    break
                try:
                    line = await _asyncio.wait_for(
                        proc.stdout.readline(), timeout=remaining
                    )
                except TimeoutError:
                    timed_out = True
                    break
                if not line:
                    break
                text = line.decode("utf-8", errors="replace").rstrip("\r\n")
                if not text:
                    continue

                try:
                    _parsed = _adapter.parse_stdout_line(text)
                    if _parsed is not None:
                        _kind, _payload, _route = _parsed
                        await _mom_publisher.publish(
                            session_id=session_id,
                            instance_id=instance_id,
                            direction=Direction.OUT,
                            source=f"agent:{instance_id}",
                            kind=_kind,
                            payload=_payload,
                            route=_route,
                            parent_msg_id=_in_msg_id,
                        )
                except Exception as _exc:
                    _log.warning("mom.publish_out_failed", error=str(_exc), text=text[:100])

                try:
                    yield _json.loads(text)
                except _json.JSONDecodeError:
                    yield {"type": "raw", "data": text}
        except _asyncio.CancelledError:
            proc.kill()
            await proc.wait()
            raise

        if timed_out:
            yield {"type": "error", "message": "Timeout"}
            proc.kill()
        await proc.wait()

        exit_code = proc.returncode or 0

        try:
            if timed_out:
                await _mom_publisher.publish(
                    session_id=session_id,
                    instance_id=instance_id,
                    direction=Direction.OUT,
                    source="system",
                    kind=Kind.ERROR,
                    payload={"message": "timeout", "timeout_seconds": timeout_seconds},
                    parent_msg_id=_in_msg_id,
                )
            elif exit_code != 0:
                await _mom_publisher.publish(
                    session_id=session_id,
                    instance_id=instance_id,
                    direction=Direction.OUT,
                    source="system",
                    kind=Kind.ERROR,
                    payload={
                        "message": f"container exited with code {exit_code}",
                        "exit_code": exit_code,
                    },
                    parent_msg_id=_in_msg_id,
                )
        except Exception as _exc:
            _log.warning("mom.publish_result_failed", error=str(_exc))

        yield {
            "type": "done",
            "status": "success" if exit_code == 0 else "failure",
            "exit_code": exit_code,
        }
    finally:
        # Best-effort: if the container is still around, force-remove it.
        if cleanup:
            try:
                _aio_cleanup = _aiodocker.Docker()
                try:
                    try:
                        container = await _aio_cleanup.containers.get(name)
                        await container.delete(force=True)
                    except _aiodocker.exceptions.DockerError:
                        pass
                finally:
                    await _aio_cleanup.close()
            except Exception:
                pass
            _shutil.rmtree(tmp_dir, ignore_errors=True)


async def stop(container_id: str) -> None:
    """Stop and remove a container by its Docker id."""
    docker = aiodocker.Docker()
    try:
        try:
            container = docker.containers.container(container_id=container_id)
            # Confirm it is agflow-managed before touching it.
            inspect = await container.show()
        except aiodocker.exceptions.DockerError as exc:
            if exc.status == 404:
                raise ContainerNotFoundError(
                    f"Container '{container_id}' not found"
                ) from exc
            raise

        labels = (inspect.get("Config") or {}).get("Labels") or {}
        if labels.get(_AGFLOW_MANAGED_LABEL) != "true":
            raise ContainerNotFoundError(
                f"Container '{container_id}' is not managed by agflow"
            )

        try:
            await container.stop(timeout=10)
        except aiodocker.exceptions.DockerError:
            # Already stopped or in a weird state — still try to remove.
            pass
        try:
            await container.delete(force=True)
        except aiodocker.exceptions.DockerError as exc:
            # 409 = "removal already in progress" (parallel click / polling
            # double-trigger). 404 = already gone. Both mean the container is
            # on its way out, treat as success.
            if exc.status not in (404, 409):
                raise
        _log.info("container.stop", container_id=container_id)
    finally:
        await docker.close()


async def publish_instruction_via_mom(
    *,
    session_id: str,
    instance_id: str,
    instruction: str,
) -> str:
    """Publish an IN instruction to the MOM bus.

    Returns the msg_id. This is a building block for the future
    MOM-driven task runner that will replace the subprocess-based flow.
    Full integration (dispatcher + aiodocker container wrapper) is
    tracked as a follow-up.
    """
    from agflow.db.pool import get_pool
    from agflow.mom.envelope import Direction, Kind
    from agflow.mom.publisher import MomPublisher

    pool = await get_pool()
    publisher = MomPublisher(
        pool=pool,
        groups_config={
            Direction.IN: ["dispatcher"],
            Direction.OUT: ["ws_push", "router"],
        },
    )
    msg_id = await publisher.publish(
        session_id=session_id,
        instance_id=instance_id,
        direction=Direction.IN,
        source="system",
        kind=Kind.INSTRUCTION,
        payload={"text": instruction},
    )
    return str(msg_id)
